package Arithmetic1;

public class addSub
{
   int num1,num2,res;
   
   public int sum(int a,int b)
   {
     num1=a;
     num2=b;
     res=a+b;
     return res;
   }
   
   public int sub(int a,int b)
      {
        num1=a;
        num2=b;
        res=a-b;
        return res;
      }
   

}